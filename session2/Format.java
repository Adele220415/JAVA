public class Format {

    public static void main(String[] args) {
        
        int age = 25;
        double height = 5.9;
        char grade = 'A';
        String name = "John Doe";
        System.out.printf("Name = %s\n", name);
        System.out.printf("Age: %d\n", age);
        System.out.printf("Height: %0.1f\n", height);
        System.out.printf("Grade = %c\n", grade);
    }
}
